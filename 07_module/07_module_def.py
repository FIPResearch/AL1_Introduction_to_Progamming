# --------------------------------------------------
# 第１の処理：果物の検索
# --------------------------------------------------

# リストの作成
List1 = ['りんご','オレンジ','さくらんぼ','ぶどう']

# 果物の指定
Name1 = 'マンゴー'

# リストの中に指定した果物があるかどうか確認する．
Check = 0
for item in List1:
	if item == Name1:
		Check = 1		# 指定した果物があればCkeck=1，なければ0のまま

# 結果の表示
if Check == 0:
	print('この中に{0}はありません．'.format(Name1))
else:
	print('この中に{0}はあります．'.format(Name1))

